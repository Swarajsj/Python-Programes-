# %%
# Counting Operator

l1 = ['A', 'B', 'C', 'D', 'E', 'A', 'F', 'A']
l1.count('A')
# %%
# Copy Operator

l2 = ['X', 'Y', 'Z']
l2 = l1.copy()
l2
# %%
# Extend Operator

l3 = ['X', 'Y', 'Z']
l4 = [1, 2, 3]
l3.extend(l4)
l3
# %%
# Index Operator

l5 = ['y', 'x', 'z', 'x']
l5.index('x')
# %%
# Insert operator

l6 = ['x', 'y', 'z']
l6.insert(1, 50)
l6
